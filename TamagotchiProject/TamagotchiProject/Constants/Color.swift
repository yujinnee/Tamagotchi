import UIKit

enum TMUIColor{
    static let backgroundColor = TMColorPallete.lightBlue
    static let fontColor = TMColorPallete.heavyBlue
    static let borderColor = TMColorPallete.heavyBlue
    
}

enum TMColorPallete {
    static let lightBlue = UIColor(red: 245/255, green: 252/255, blue: 252/255, alpha: 1)
    static let heavyBlue = UIColor(red: 77/255, green: 106/255, blue: 120/255, alpha: 1)
}
